/**
 * 書類生成用のプロンプトコンテキスト
 *
 * system はタイプ別の指示、prompt は棚卸し結果+求人情報を含む入力データ。
 * モデルへは「system に役割、prompt にデータ」の役割分担で渡す。
 */
export function buildDocumentPrompt(params: {
  type: DocumentType;
  profile: CareerProfile;
  jobInfo?: string;
  customInstructions?: string;
}): { system: string; prompt: string } {
  const system = DOCUMENT_PROMPTS[params.type];

  const promptParts: string[] = [
    "以下は、ユーザーのキャリア棚卸し結果です。",
    "",
    "【キャリア棚卸し結果】",
    JSON.stringify(params.profile, null, 2),
  ];

  if (params.jobInfo) {
    promptParts.push("", "【求人情報】", params.jobInfo);
  }

  if (params.customInstructions) {
    promptParts.push("", "【追加の指示】", params.customInstructions);
  }

  promptParts.push("", "上記を踏まえて、書類を生成してください。");

  return {
    system,
    prompt: promptParts.join("\n"),
  };
}
