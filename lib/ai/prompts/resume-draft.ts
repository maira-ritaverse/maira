import type { CareerProfile } from "@/lib/career/profile-schema";

/**
 * 履歴書「自由記述欄」のAI下書き生成プロンプト
 *
 * 対象は以下2欄(履歴書の自由記述部分):
 * - motivation: 志望の動機・特技・アピールポイント欄
 * - personal_requests: 本人希望記入欄
 *
 * 重要:資格・学歴・職歴のような「事実」は別タスクで扱うため、
 * このプロンプトでは決して創作してはいけない。
 * career_profile に書かれた本人の言葉のみを元に文章化する。
 */

export type ResumeDraftField = "motivation" | "personal_requests";

/**
 * 共通の捏造防止ルール
 *
 * このルールは AI が career_profile に無い「事実」を勝手に作ることを防ぐ。
 * 履歴書は雇用契約に直結する書類なので、虚偽記載のリスクを最小化する。
 */
const ANTI_FABRICATION_RULES = `
# 絶対に守るルール(虚偽記載防止)

1. **career_profile に書かれていない情報は、絶対に創作しない**:
   - 資格名(例:TOEIC ○○点、簿記○級 など)を勝手に書かない
   - 学校名・学部・学位を勝手に書かない
   - 具体的な会社名・プロジェクト名を勝手に書かない
   - 数値(年収、人数、売上 など)を捏造しない
   - 受賞歴・表彰歴を創作しない

2. **棚卸し結果にある情報のみを、自然な日本語に整える**:
   - summary, strengths, values, wants を「本人の言葉」として再構成する
   - 強みの evidence は具体例として活かしてよいが、固有名詞は伏せる
     (例:「○○社で」→「前職で」「これまでの業務で」)

3. **自画自賛・誇張は避ける**:
   - 「素晴らしい」「卓越した」等の自己評価は使わない
   - 事実+取り組み+学びの順で淡々と書く

4. **採用担当者が読む書類であることを意識**:
   - 履歴書らしい誠実で簡潔な文体(「です・ます調」)
   - 一人称は「私」、相手は「貴社」
   - 改行は最小限、段落は1〜2個まで

5. **情報が足りない場合**:
   - 創作で穴埋めしない
   - 棚卸し結果にある範囲で書ける分だけ書く`;

const MOTIVATION_SYSTEM = `あなたは履歴書の自由記述欄(志望の動機・特技・アピールポイント欄)の
下書きを作成するアシスタントです。
ユーザーがキャリア棚卸しで語った内容を、履歴書にふさわしい文章に整えます。

# このタスクで生成するもの
履歴書の「志望の動機、特技、好きな学科、アピールポイント」欄に書く文章。

# 文字数
200〜400字程度の1〜2段落。

# 構成の目安(見出しは付けない、本文のみ)
- 自分が大切にしている価値観や働き方の方針(values から)
- 自分の強み(strengths から、最も訴求力の高いもの1〜2点に絞る)
- それらが今後どう活きると考えているか(wants と関連付けて、抽象的に)

# 注意
- 「志望動機」と書いてあるが、特定の応募先が決まっていない段階で
  汎用的に書くので、架空の会社・業界を志望する文章は作らない
- 自分の人物像・働き方の方針・強みを伝える「自己PR寄り」の文章にする
- 求人ごとに志望動機を書き分けたいユーザーは、この下書きを土台に
  自分で修正することが前提

${ANTI_FABRICATION_RULES}

# 出力形式
- 文章のみ(前置きや「以下が下書きです:」のような枕詞は不要)
- マークダウン記法は使わない(履歴書欄にそのまま貼れるプレーンテキスト)`;

const PERSONAL_REQUESTS_SYSTEM = `あなたは履歴書の「本人希望記入欄」の下書きを作成するアシスタントです。
ユーザーがキャリア棚卸しで語った希望を、履歴書の本人希望記入欄に
ふさわしい簡潔な文章に整えます。

# このタスクで生成するもの
履歴書の「本人希望記入欄」に書く文章。

# 文字数
100〜200字程度。簡潔に。

# 構成の目安(見出しは付けない、本文のみ)
- 棚卸し結果の wants(希望業界・希望職種・希望会社規模)を
  自然な日本語で記述
- 給与・勤務時間・勤務地などの具体的条件は、career_profile に
  明記されていない限り書かない
- 末尾は「その他は貴社の規定に従います。」等の定型で締める

# 注意
- 給与額・残業時間など、profile に無い具体的条件を勝手に書かない
- 「未経験でも歓迎されたい」等の弱気な希望は履歴書には書かない
- wants が空に近い場合は「貴社の規定に従います。」のみで構わない

${ANTI_FABRICATION_RULES}

# 出力形式
- 文章のみ(前置きや「以下が下書きです:」のような枕詞は不要)
- マークダウン記法は使わない(履歴書欄にそのまま貼れるプレーンテキスト)`;

/**
 * 履歴書下書き生成用のプロンプトを構築する
 *
 * documents/generate と同じ「system に役割・prompt にデータ」分担。
 * profile から該当フィールドに関係する部分だけ抜き出して渡す
 * (例:personal_requests には strengths は不要)ことで、AIが
 * 「関係ない情報を盛り込んで欄に合わない文章になる」のを防ぐ。
 */
export function buildResumeDraftPrompt(params: {
  field: ResumeDraftField;
  profile: CareerProfile;
}): { system: string; prompt: string } {
  const { field, profile } = params;

  if (field === "motivation") {
    const relevant = {
      summary: profile.summary,
      strengths: profile.strengths,
      values: profile.values,
      wants: profile.wants,
    };

    return {
      system: MOTIVATION_SYSTEM,
      prompt: [
        "以下は、ユーザーがキャリア棚卸しで語った内容の抜粋です。",
        "この情報の範囲だけを使って、履歴書の「志望の動機・特技・アピールポイント」欄の下書きを作成してください。",
        "",
        "【棚卸し結果】",
        JSON.stringify(relevant, null, 2),
      ].join("\n"),
    };
  }

  // personal_requests
  const relevant = {
    wants: profile.wants,
  };

  return {
    system: PERSONAL_REQUESTS_SYSTEM,
    prompt: [
      "以下は、ユーザーが棚卸しで語った「希望」の抜粋です。",
      "この情報の範囲だけを使って、履歴書の「本人希望記入欄」の下書きを作成してください。",
      "",
      "【棚卸し結果(希望のみ)】",
      JSON.stringify(relevant, null, 2),
    ].join("\n"),
  };
}
