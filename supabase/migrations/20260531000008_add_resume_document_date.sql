-- ============================================
-- 履歴書「作成日(現在)」カラム追加
--
-- 履歴書の右上に表示する「○年○月○日 現在」の日付を、
-- これまでは出力時の本日に自動設定していた。しかし実運用では
-- 「下書きを保存しておいて、後日まとめて提出する」など、
-- ユーザーが意図した提出日を入れたいケースが多い。
--
-- そこで明示的に保存できる document_date カラムを追加する。
-- 未入力(null)時は従来通り「今日の日付」をフォールバックで表示する。
--
-- nullable で追加し、既存レコードを壊さない。
-- ============================================

alter table public.resumes
  add column if not exists document_date date;

comment on column public.resumes.document_date is '履歴書の「○年○月○日 現在」として表示する作成日/提出日。null の場合は表示時に本日の日付にフォールバック。';
